// 功能：输出1-100之间的不能被5整除的数，每5个一行

public class HomeWork03 {
    public static void main(String[] args) {
        for (int i = 1, n = 0; i <= 100; i++) {
            if (i % 5 != 0) {
                System.out.print(i + "\t");
                n++;
                if (n % 5 == 0) {
                    System.out.println("");
                }
            }
        }
        
    }
}
