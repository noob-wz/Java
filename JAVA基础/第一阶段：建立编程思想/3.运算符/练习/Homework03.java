// 功能：计算对应表达式的结果
// 目的：熟悉算数运算符的真正用法

public class Homework03 {
    public static void main(String[] args) {

        System.out.println(10/3); // 3
        System.out.println(10/5); // 2
        System.out.println(10%2); // 0
        System.out.println(-10.5%3); // -1.5 。取余结果的符号和被除数符号相同（a%b= a - a/b*b)
    }
}
